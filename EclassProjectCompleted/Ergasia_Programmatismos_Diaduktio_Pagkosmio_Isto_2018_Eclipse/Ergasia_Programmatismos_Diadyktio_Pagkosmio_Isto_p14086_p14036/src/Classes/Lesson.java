package Classes;

public class Lesson {
	private int grade;
	private String id_Lnumber;
	private String Lessonname;

	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

	public String getId_Lnumber() {
		return id_Lnumber;
	}

	public void setId_Lnumber(String id_Lnumber) {
		this.id_Lnumber = id_Lnumber;
	}

	public String getLessonname() {
		return Lessonname;
	}

	public void setLessonname(String lessonname) {
		Lessonname = lessonname;
	}
}
